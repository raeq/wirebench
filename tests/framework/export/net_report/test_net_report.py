"""Net report renderer — output regression + sanity tests.

The "golden source" for the byte-identical regression is the
per-demo `docs/<Class>.net-report.md` file generated by
`scripts/render_demo_docs.py`.  This keeps the in-repo demo
artefacts and the test corpus in lock-step without duplicating the
files under `tests/golden/`.
"""
