def _silently(fn, *a, **k):
    with warnings.catch_warnings():
        warnings.simplefilter('ignore')
        return fn(*a, **k)


def _construct(cls):
    """Match the regenerator's two-shape construction probe."""
    try:
        return _silently(cls)
    except Exception:
        try:
            return _silently(cls, refdes_number=1)
        except Exception:
            return None


def _iter_golden_pairs():
    """Yield `(demo, class_name, cls, golden_path)` for every committed
    `*.net-report.md` under demos/*/docs/."""
    for demo_dir in sorted(_DEMOS.iterdir()):
        if not demo_dir.is_dir() or demo_dir.name.startswith('_'):
            continue
        docs = demo_dir / 'docs'
        if not docs.is_dir():
            continue
        for py_file in sorted(demo_dir.glob('*.py')):
            if py_file.name.startswith('_'):
                continue
            mod_name = py_file.stem
            try:
                mod = __import__(mod_name)
            except Exception:
                continue
            for name in sorted(dir(mod)):
                obj = getattr(mod, name)
                if not (isinstance(obj, type)
                        and issubclass(obj, Circuit)
                        and obj.__module__ == mod_name):
                    continue
                golden = docs / f"{name}.net-report.md"
                if golden.exists():
                    yield demo_dir.name, name, obj, golden


@pytest.mark.parametrize(
    "demo,class_name,cls,golden",
    list(_iter_golden_pairs()),
    ids=lambda x: x if isinstance(x, str) else getattr(x, '__name__', repr(x)),
)
def test_net_report_matches_committed_demo_output(
    demo, class_name, cls, golden,
):
    """Re-rendering each demo's net report produces byte-identical
    output to the file checked into `demos/<demo>/docs/`."""
    design = _construct(cls)
    if design is None:
        pytest.skip(f"{class_name} cannot be constructed without args")
    actual = export_to_string(design, 'net_report')
    expected = golden.read_text()
    assert actual == expected, (
        f"{demo}/{class_name}.net-report.md drift — re-run "
        f"`uv run python scripts/render_demo_docs.py` to refresh."
    )


# ----------------------------------------------------- structural sanity


def test_hello_led_has_three_nets_and_pull_up_path():
    from hello_led import HelloLED
    out = export_to_string(_silently(HelloLED), 'net_report')
    assert "# Net Report — HelloLED" in out
    assert "3 logical net(s)" in out
    # The 330 Ω resistor bridging VCC to D1 should be detected as a
    # pull-up path on the LED's anode net.
    assert "Pull-up path:" in out
    assert "→ + rail via R1" in out


def test_renderer_classifies_drivers_and_readers():
    from hello_led import HelloLED
    out = export_to_string(_silently(HelloLED), 'net_report')
    # Rails drive the +/− nets; the LED is a reader on each.
    assert "Drivers (1):" in out
    assert "Readers (1):" in out


def test_isolated_design_shows_two_domains():
    from isolated_rs232 import IsolatedRS232Link
    out = export_to_string(_silently(IsolatedRS232Link), 'net_report')
    assert "ELECTRICAL" in out
    assert "ISOLATED_RS232" in out
